public class Calculadora {
    int somar(int numero , int numero2) {
        return numero + numero2;
    }
    int subtrair(int numero , int numero2){
        return numero - numero2;
    }
    int multiplicacao(int numero ,int numero2){
        return numero * numero2;
    }
    double divisao(double numero , double numero2){
        return numero / numero2;
    }
    int modulo(int numero , int numero2){
        return numero % numero2;
    }
    
}